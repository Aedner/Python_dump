n1=int(input("enter 1st number"))
n2=int(input("enter 2nd number"))
opr=str(input("Which operation do you wanna perform? (+ - * /)"))
if(opr=='+'):
    print("Your answer is",n1+n2)
if(opr=='-'):
    print("Your answer is",n1-n2)
if(opr=='*'):
    print("Your answer is",n1*n2)
if(opr=='/'):
    print("Your answer is",n1/n2)
